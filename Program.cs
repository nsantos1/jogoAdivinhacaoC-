    int numeroAleatorio = random.Next(1, 100);
    
    do
    {
        Console.Write("\nChute um valor entre 1 e 100:  ");
        string chute = Console.ReadLine()!;
        int valorChute = int.Parse(chute);

        if (valorChute == numeroAleatorio)
        {
            Console.WriteLine($"\nMeus parabéns!! Você ganhou, o número aleatório era: {numeroAleatorio}");
            break;
        }
        else if (valorChute < numeroAleatorio)
        {
            Console.WriteLine("\nO número aleatório é maior");
        }
        else
        {
            Console.WriteLine("\nO número aleatório é menor");
        }

    } while (true);

}

